#include<iostream>
#include<math.h>
using namespace std;
int main()
{
    int num = 4;
    cout<<"Square root :"<<sqrt(num)<<endl;

    cout<<"Sin :"<<sin(num)<<endl;

    cout<<"Cos :"<<cos(num)<<endl;

    cout<<"Tan :"<<tan(num)<<endl;

    cout<<"Power :"<<pow(2,3)<<endl;

    return 0;
}
